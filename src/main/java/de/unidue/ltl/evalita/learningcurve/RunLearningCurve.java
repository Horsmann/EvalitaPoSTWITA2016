    private static String num(String train)
    {
        int num=0;
        for(File f : new File(train).listFiles()){
            if(f.isHidden()){
                continue;
            }
            num++;
        }
        
        return ""+num;
    }

    private static String filterRedundantEntries(String data, Set<String> skipIds)
        throws IOException
    {
        File f = null;
        for (File content : new File(data).listFiles()) {
            if (content.isHidden()) {
                continue;
            }
            f = content;
        }

        List<String> lines = FileUtils.readLines(f, "utf-8");
        List<String> out = new ArrayList<>();

        boolean skip = false;
        for (int i = 0; i < lines.size(); i++) {
            String string = lines.get(i);
            if (string.matches("_+[0-9]+_+")) {
                if (skipIds.contains(string)) {
                    skip = true;
                }
            }
            if (string.isEmpty()) {
                skip = false;
                if (out.isEmpty() || out.get(out.size() - 1).isEmpty()) {
                    continue;
                }
            }
            if (skip) {
                continue;
            }
            out.add(string);
        }

        File dir = new File(
                System.getProperty("java.io.tmpdir") + "/evalita" + System.currentTimeMillis());
        dir.mkdirs();
        File tmp = new File(dir, "redundantFreeEvalitaData.txt");
        FileUtils.writeLines(tmp, "utf-8", out);
        return dir.getAbsolutePath();

    }

    private static Double getLastLine(File f)
        throws IOException
    {
        List<String> readLines = FileUtils.readLines(f);

        return Double.valueOf(readLines.get(readLines.size() - 1).replace(",", "."));
    }

    @SuppressWarnings("unchecked")
    public static ParameterSpace getParameterSpace(String featureMode, String learningMode,
            String train, String test)
                throws Exception
    {

        Dimension<TcFeatureSet> dimFeature = Dimension.create(DIM_FEATURE_SET,
                Features.getFeatures(brownCluster, posDictionary, namelist));

        Dimension<List<String>> dimClassificationArgs = Dimension.create(DIM_CLASSIFICATION_ARGS,
                asList(new String[] {
                        CRFSuiteAdapter.ALGORITHM_AVERAGED_PERCEPTRON, "-p", "feature.possible_states=1" }));

        // configure training and test data reader dimension
        Map<String, Object> dimReaders = new HashMap<String, Object>();
        CollectionReaderDescription trainCrd = CollectionReaderFactory.createReaderDescription(
                ItalianTokenTagReader.class, ItalianTokenTagReader.PARAM_LANGUAGE, languageCode,
                ItalianTokenTagReader.PARAM_SOURCE_LOCATION, train,
                ItalianTokenTagReader.PARAM_PATTERNS, "*.data");
        dimReaders.put(DIM_READER_TRAIN, trainCrd);

        CollectionReaderDescription testCrd = CollectionReaderFactory.createReaderDescription(
                ItalianTokenTagReader.class, ItalianTokenTagReader.PARAM_LANGUAGE, languageCode,
                ItalianTokenTagReader.PARAM_SOURCE_LOCATION, test,
                ItalianTokenTagReader.PARAM_PATTERNS, "*.data");
        dimReaders.put(DIM_READER_TEST, testCrd);

        ParameterSpace pSpace = new ParameterSpace(Dimension.createBundle("readers", dimReaders),
                Dimension.create(DIM_LEARNING_MODE, learningMode),
                Dimension.create(DIM_FEATURE_MODE, featureMode), dimFeature, dimClassificationArgs);

        return pSpace;
    }

    public static void validation(ParameterSpace pSpace)
        throws Exception
    {
        ExperimentTrainTest batch = new ExperimentTrainTest(experimentName, CRFSuiteAdapter.class);
        batch.setPreprocessing(createEngineDescription(TcPosTaggingWrapper.class));
        batch.addReport(AccuracyPerWordClass.class);
        batch.addReport(AverageCoarseAccuracyCollector.class);
        batch.addReport(KnownUnknownWordAnalysisReport.class);
        batch.setParameterSpace(pSpace);

        // Run
        Lab.getInstance().run(batch);
    }

